#include<iostream>
using namespace std;
/*让我们用字母B来表示“百”、字母S表示“十”，用“12...n”来表示个位数字n（<10），
换个格式来输出任一个不超过3位的正整数。例如234应该被输出为BBSSS1234，因为它
有2个“百”、3个“十”、以及个位的4。

输入格式：每个测试输入包含1个测试用例，给出正整数n（<1000）。

输出格式：每个测试用例的输出占一行，用规定的格式输出n。

输入样例1：
234
输出样例1：
BBSSS1234
输入样例2：
23
输出样例2：
SS123
提交代码*/

//纯粹弱智题，无解。。。
int main()
{
	int a;
	cin >> a;
	for (int i = 1; i <= a / 100; i++)
	{
		cout << "B";
	}
	a = a - a / 100 * 100;
	for (int i = 1; i <= a / 10; i++)
	{
		cout << "S";
	}
	a = a % 10;
	for (int i = 1; i <= a; i++)
	{
		cout << i;
	}
	cout << endl;
	system("pause");
	return 0;
}